export const decodeBits = (bits: string) => {
  bits = bits.replace(/^0+|0+$/g, "");
  const len = bits.match(/0+|1+/g)?.sort((a, b) => a.length - b.length)[0].length ?? 1;
  return bits
    .replaceAll("0".repeat(7 * len), "   ")
    .replaceAll("1".repeat(3 * len), "-")
    .replaceAll("0".repeat(3 * len), " ")
    .replaceAll("1".repeat(len), ".")
    .replaceAll("0".repeat(len), "");
}; // decodeBits()

export const decodeMorse = (morseCode: string) => {
  return morseCode.split("   ").map((w) => w.split(" ").map((l) => MORSE_CODE[l]).join("")).join(" ");
}; // decodeMorse()

